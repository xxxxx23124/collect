import os
import random
from pathlib import Path
from typing import Iterable, Optional, Sequence, Union

from PIL import Image
from torch.utils.data import Dataset
from torchvision import transforms


class RandomDihedralTransform:
    """Randomly apply one of the 8 rotations/reflections of a square image."""

    _operations = (
        None,
        Image.Transpose.ROTATE_90,
        Image.Transpose.ROTATE_180,
        Image.Transpose.ROTATE_270,
        Image.Transpose.FLIP_LEFT_RIGHT,
        Image.Transpose.FLIP_TOP_BOTTOM,
        Image.Transpose.TRANSPOSE,
        Image.Transpose.TRANSVERSE,
    )

    def __call__(self, image: Image.Image) -> Image.Image:
        operation = random.choice(self._operations)
        if operation is None:
            return image
        return image.transpose(operation)


class FractalDataset(Dataset):
    """Dataset for images generated by experiment/DDPM/fractal_generator.py."""

    def __init__(
        self,
        root: Union[str, os.PathLike] = None,
        image_size: int = 512,
        extensions: Optional[Sequence[str]] = None,
        random_horizontal_flip: bool = True,
        random_geometric_augment: bool = True,
    ):
        super().__init__()
        if root is None:
            root = Path(__file__).resolve().parents[1] / "generated_fractals" / "images"
        self.root = Path(root)
        self.extensions = tuple(ext.lower() for ext in (extensions or (".png", ".jpg", ".jpeg", ".bmp")))
        self.image_paths = self._scan_images(self.root)

        transform_steps = [transforms.Resize((image_size, image_size))]
        if random_geometric_augment:
            transform_steps.append(RandomDihedralTransform())
        elif random_horizontal_flip:
            transform_steps.append(transforms.RandomHorizontalFlip(p=0.5))
        transform_steps.extend(
            [
                transforms.ToTensor(),
                transforms.Normalize([0.5, 0.5, 0.5], [0.5, 0.5, 0.5]),
            ]
        )
        self.transform = transforms.Compose(transform_steps)

        print(f"Found {len(self.image_paths)} fractal images in: {self.root}")

    def _scan_images(self, root: Path) -> list[Path]:
        if not root.exists():
            print(f"Warning: fractal image folder not found: {root}")
            return []
        return sorted(path for path in root.rglob("*") if path.suffix.lower() in self.extensions)

    def __len__(self):
        return len(self.image_paths)

    def __getitem__(self, idx):
        if not self.image_paths:
            raise RuntimeError(f"No images found in {self.root}. Run fractal_generator.py first.")

        path = self.image_paths[idx]
        try:
            image = Image.open(path).convert("RGB")
            return self.transform(image)
        except Exception as exc:
            print(f"Error loading image: {path}, Error: {exc}")
            return self.__getitem__((idx + 1) % len(self))


